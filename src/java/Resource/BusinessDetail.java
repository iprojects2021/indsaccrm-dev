public class BusinessDetail {
    
    private String gstin;
    private String ownerdetails;
    private String companystatus;
    private String businesstype;
    private String totalemployee;
    private String yearofestablished;
    private String grossannualturnover;
    private String annualexportturnover;

    public String getGstin() {
        return gstin;
    }

    public void setGstin(String gstin) {
        this.gstin = gstin;
    }

    public String getOwnerdetails() {
        return ownerdetails;
    }

    public void setOwnerdetails(String ownerdetails) {
        this.ownerdetails = ownerdetails;
    }

    public String getCompanystatus() {
        return companystatus;
    }

    public void setCompanystatus(String companystatus) {
        this.companystatus = companystatus;
    }

    public String getBusinesstype() {
        return businesstype;
    }

    public void setBusinesstype(String businesstype) {
        this.businesstype = businesstype;
    }

    public String getTotalemployee() {
        return totalemployee;
    }

    public void setTotalemployee(String totalemployee) {
        this.totalemployee = totalemployee;
    }

    public String getYearofestablished() {
        return yearofestablished;
    }

    public void setYearofestablished(String yearofestablished) {
        this.yearofestablished = yearofestablished;
    }

    public String getGrossannualturnover() {
        return grossannualturnover;
    }

    public void setGrossannualturnover(String grossannualturnover) {
        this.grossannualturnover = grossannualturnover;
    }

    public String getAnnualexportturnover() {
        return annualexportturnover;
    }

    public void setAnnualexportturnover(String annualexportturnover) {
        this.annualexportturnover = annualexportturnover;
    }
    
    
    
    
}
